package net.tao.rhb.dd.commons.constant;

public enum TransactionType {
    OUTWARD_DN("outward_dn"),

    INWARD_DN("inward_dn");

    private final String value;

    TransactionType(String value) {
        this.value = value;
    }

    public String value() {
        return value;
    }

    @Override
    public String toString() {
        return "TransactionType{"
               + "value='" + value + '\''
               + '}';
    }
}
